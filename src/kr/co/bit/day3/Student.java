package kr.co.bit.day3;

public class Student {
	private String scn;
	private String mail;
	private String kor;
	private String eng;
	private String mat;
	private String sic;
	private String sos;
	private String total;
	private String tic;
	private String acm;
	private String code;
	
	public Student() {}

	

	



	public Student(String scn, String mail, String kor, String eng, String mat, String sic, String sos, String total,
			String tic, String acm, String code) {
		super();
		this.scn = scn;
		this.mail = mail;
		this.kor = kor;
		this.eng = eng;
		this.mat = mat;
		this.sic = sic;
		this.sos = sos;
		this.total = total;
		this.tic = tic;
		this.acm = acm;
		this.code = code;
	}







	public String getScn() {
		return scn;
	}



	public void setScn(String scn) {
		this.scn = scn;
	}



	public String getMail() {
		return mail;
	}



	public void setMail(String mail) {
		this.mail = mail;
	}



	public String getKor() {
		return kor;
	}



	public void setKor(String kor) {
		this.kor = kor;
	}



	public String getEng() {
		return eng;
	}



	public void setEng(String eng) {
		this.eng = eng;
	}



	public String getSic() {
		return sic;
	}



	public void setSic(String sic) {
		this.sic = sic;
	}



	public String getMat() {
		return mat;
	}



	public void setMat(String mat) {
		this.mat = mat;
	}



	public String getSos() {
		return sos;
	}



	public void setSos(String sos) {
		this.sos = sos;
	}



	public String getTotal() {
		return total;
	}



	public void setTotal(String total) {
		this.total = total;
	}



	public String getTic() {
		return tic;
	}



	public void setTic(String tic) {
		this.tic = tic;
	}



	public String getAcm() {
		return acm;
	}



	public void setAcm(String acm) {
		this.acm = acm;
	}



	public String getCode() {
		return code;
	}



	public void setCode(String code) {
		this.code = code;
	}



	
	
}
